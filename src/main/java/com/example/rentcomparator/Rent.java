package main.java.com.example.rentcomparator;

/**
 * @author AbhishekMishra
 *
 */
public class Rent {
	
	private double flatMonthlyRent;
	private double rentedYear;
	private double monthlyMaintenance;
	private double increaseRate;

	public double getFlatMonthlyRent() {
		return flatMonthlyRent;
	}

	public void setFlatMonthlyRent(double flatMonthlyRent) {
		this.flatMonthlyRent = flatMonthlyRent;
	}

	public double getRentedYear() {
		return rentedYear;
	}

	public void setRentedYear(double rentedYear) {
		this.rentedYear = rentedYear;
	}

	public double getMonthlyMaintenance() {
		return monthlyMaintenance;
	}

	public void setMonthlyMaintenance(double monthlyMaintenance) {
		this.monthlyMaintenance = monthlyMaintenance;
	}

	public double getIncreaseRate() {
		return increaseRate;
	}

	public void setIncreaseRate(double increaseRate) {
		this.increaseRate = increaseRate;
	}
	
}
