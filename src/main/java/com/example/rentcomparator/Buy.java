public class Buy {
	
	private double flatPrice;
	private double purchaseYear;
	private double monthlyMaintenance;
	private double maintenaceIncreaseRate;
	
	public double getFlatPrice() {
		return flatPrice;
	}
	public void setFlatPrice(double flatPrice) {
		this.flatPrice = flatPrice;
	}
	public double getPurchaseYear() {
		return purchaseYear;
	}
	public void setPurchaseYear(double purchaseYear) {
		this.purchaseYear = purchaseYear;
	}
	public double getMonthlyMaintenance() {
		return monthlyMaintenance;
	}
	public void setMonthlyMaintenance(double monthlyMaintenance) {
		this.monthlyMaintenance = monthlyMaintenance;
	}
	public double getMaintenaceIncreaseRate() {
		return maintenaceIncreaseRate;
	}
	public void setMaintenaceIncreaseRate(double maintenaceIncreaseRate) {
		this.maintenaceIncreaseRate = maintenaceIncreaseRate;
	}
	
	

}
